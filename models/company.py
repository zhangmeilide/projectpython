    capital_value = Column(Float, nullable=False, default=0, comment='注册资本数值')
    capital_value_rmb = Column(Float, nullable=False, default=0, comment='注册资本人民币值')
    company_status = Column(Integer, nullable=True, default=0, comment='主体状态')
