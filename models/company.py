    updated_at = Column(DateTime, nullable=True, comment='更新时间')
    deleted_at = Column(DateTime, nullable=True, comment='删除时间')
    lat = Column(String(32), nullable=True, comment='纬度')
    lng = Column(String(32), nullable=True, comment='经度')
    back_flag = Column(SmallInteger, nullable=False, default=0, comment='退回标识')
    unusual_flag = Column(SmallInteger, nullable=False, default=1, comment='退回状态')
    dead_flag = Column(SmallInteger, nullable=False, default=1, comment='退回状态')
    label_names = Column(String(500), nullable=True, comment='标签名称')
    collect_flag = Column(SmallInteger, nullable=False, default=0, comment='收藏标识')
    patrol_time = Column(DateTime, nullable=True, comment='巡查时间')
    patrol_num = Column(Integer, nullable=False, default=0, comment='巡查次数')
    judge_num = Column(Integer, nullable=False, default=0, comment='判定次数')
    judge_time = Column(DateTime, nullable=True, comment='判定时间')
    attention_flag = Column(SmallInteger, nullable=False, default=0, comment='是否关注')
    company_pripid = Column(String(100), nullable=True)
    part_business_scope = Column(Text, nullable=True)
    check_date = Column(DateTime, nullable=True)
    administer_unit = Column(String(50), nullable=True)
    legal_idcard = Column(String(50), nullable=True)
    legal_mobile = Column(Integer, nullable=True)
    employed_num = Column(Integer, nullable=False, default=0)
    flexible_employ_flag = Column(SmallInteger, nullable=False, default=0, comment='灵活用工企业标识')
    business_address = Column(Text, nullable=True, comment='经营场所')
    actual_business_address = Column(Text, nullable=True, comment='实际经营地址')
    compare_flag = Column(SmallInteger, nullable=False, default=0, comment='比对标示')
    regorg = Column(Integer, nullable=False, default=0, comment='登记机关')
    localadm = Column(Integer, nullable=False, default=0, comment='管辖单位')
    currency = Column(Integer, nullable=False, default=0, comment='单位')
    enttype = Column(Integer, nullable=False, default=0, comment='企业类型')
    industryphy = Column(String(100), nullable=True, default='0', comment='行业编号')
    ptbusscope = Column(Text, nullable=True, comment='子经营范围')
    hzrq = Column(DateTime, nullable=True, comment='核准日期')
    cerno = Column(String(50), nullable=True, comment='身份证')
    history_name = Column(String(128), nullable=True, comment='主体名称')
    check_flag = Column(SmallInteger, nullable=False, default=0, comment='小程序核查状态')
    org_name = Column(String(100), nullable=True, comment='管辖单位名称')
    localadm_sj = Column(Integer, nullable=False, default=0, comment='上级管辖单位编号')
    industry_ids = Column(Integer, nullable=False, default=0, comment='行业编号')
    company_uuid = Column(String(200), nullable=True, comment='企业唯一标识')
    sync_flag = Column(SmallInteger, nullable=False, default=0, comment='数据同步状态')
    website_flag = Column(SmallInteger, nullable=False, default=0, comment='交易网站标识')
