    label_num = Column(SmallInteger, nullable=False, default=0, comment='标签数量')
    risk_level = Column(String(1), nullable=True, comment='风险等级')
    credit_level = Column(String(1), nullable=True, comment='信用等级')
    xzxk = Column(SmallInteger, nullable=False, default=0, comment='行政许可')
