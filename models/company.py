    current_loc = Column(String(255), nullable=True, comment='当前地址')
    order_index = Column(Integer, nullable=False, default=3, comment='排序')
    province = Column(String(100), nullable=True, comment='省')
    province_id = Column(Integer, nullable=False, default=0, comment='省机构id')
    city = Column(String(100), nullable=True, comment='市')
    city_id = Column(Integer, nullable=False, default=0, comment='市机构id')
    county = Column(String(100), nullable=True, comment='区')
    county_id = Column(Integer, nullable=False, default=0, comment='区机构id')
    street = Column(String(100), nullable=True, comment='街道')
