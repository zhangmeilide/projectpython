    personal_flag = Column(SmallInteger, nullable=False, default=0, comment='个人标识')
    establish_date = Column(String(64), nullable=True, comment='有效期')
    legal_person = Column(String(64), nullable=True, comment='法人')
    operation_start_date = Column(String(64), nullable=True, comment='起始时间')
    operation_end_date = Column(String(64), nullable=True, comment='结束时间')
    data_source = Column(SmallInteger, nullable=False, default=1, comment='数据来源')
    remark = Column(Text, nullable=True, comment='备注信息')
    industry = Column(String(64), nullable=True, comment='所属行业')
    industry_id = Column(Integer, nullable=False, default=0, comment='行业id')
    link_man = Column(String(64), nullable=True, comment='联系人')
    link_phone = Column(String(64), nullable=True, comment='联系电话')
    link_email = Column(String(64), nullable=True, comment='联系邮箱')
    area_id = Column(Integer, nullable=False, default=0, comment='所属地区id')
    user_id = Column(Integer, nullable=True, comment='操作人')
    trade_flag = Column(SmallInteger, nullable=False, default=0, comment='交易标识')
    is_online = Column(SmallInteger, nullable=False, default=0, comment='是否在网')
    company_id_old = Column(Integer, nullable=True, comment='旧company_id')
