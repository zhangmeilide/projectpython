    rand_num = Column(Integer, nullable=False, default=0, comment='随机数量')
    check_time = Column(DateTime, nullable=True, comment='检查时间')
    official_website = Column(String(255), nullable=True, comment='官网')
    credit_score = Column(Integer, nullable=False, default=0, comment='信用得分')
    risk_score = Column(Integer, nullable=False, default=0, comment='风险得分')
    live_flag = Column(SmallInteger, nullable=False, default=0, comment='主播标识')
    user_flag = Column(SmallInteger, nullable=False, default=0, comment='平台用户标识')
    labels_industry_name = Column(String(255), nullable=True, default='', comment='产业标签')
    labels_industry_id = Column(String(255), nullable=True, default='', comment='产业标签id')
    warn_flag = Column(SmallInteger, nullable=False, default=0, comment='风险标识')
    industry_id_match = Column(Integer, nullable=False, default=0, comment='关联行业id')
    industry_id_level=Column(SmallInteger, nullable=False, default=0, comment='关联行业层级')










